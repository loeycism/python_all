# Método x Função
# Função = retorna valor
# Método (definição) = não retorna dep

def soma(a, b):
    #sempre que eu chamar o método, eu tenho que passar dois valores
    # é um bom reaproveitamento de código
    return a + b


def sub(a, b):
    return a - b

def div(a, b):
    return a / b

def multi(a, b):
    return a * b

print(soma(5, 2))
print(sub(5, 2))
print(div(5, 2))
print(multi(5, 2))